def move_san(move_full: str) -> str:
    """Extract SAN from full move string: '27... Ne7' → 'Ne7', '23. Nh6' → 'Nh6'."""
    return move_full.split()[-1]


def as_records():
    """Return list of dicts, one per position."""
    return [
        {"idx": i, "fen": FENS[i], "move_full": MOVES[i],
         "move_san": move_san(MOVES[i]), "ref": REF[i].strip(), "gac": GAC[i].strip()}
        for i in range(60)
    ]
